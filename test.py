#Написать тест по математике.
#   генерить примеры на +, -, *
# проверки хранить в виде функций
#  к функции проверки обращаться по ключу-действию, собрав их в словарь!
#   внешний цикл предлагает решить ещё один пример, либо завершить тест.
#   выводится статистика.

import random

# Функция для сложения
def сложение(a, b):
    return a + b

# Функция для вычитания
def вычитание(a, b):
    return a - b

# Функция для умножения
def умножение(a, b):
    return a * b

# Словарь с функциями проверки
операции = {
    '+': сложение,
    '-': вычитание,
    '*': умножение
}

# Функция для проверки ответа на пример
def проверить_ответ(num1, num2, операция, ответ):
    правильный_ответ = операции[операция]
    результат = правильный_ответ(num1, num2)
    return результат == ответ

# Статистика ответов
результат = {'верно': 0, 'неверно': 0}

продолжить_тест = True
while продолжить_тест:
    # Генерация случайных чисел и оператора
    num1 = random.randint(1, 10)
    num2 = random.randint(1, 10)
    операция = random.choice(list(операции))

    # Вывод примера
    print(f"Чему равно {num1} {операция} {num2}?")
      
    # Ввод ответа от пользователя
    пользовательский_ответ = int(input("Введите ваш ответ: "))

    # Проверка ответа
    if проверить_ответ(num1, num2, операция, пользовательский_ответ):
        print("Верно!\n")
        результат['верно'] += 1
    else:
        print("Неверно!\n")
        результат['неверно'] += 1

    # Запрос на продолжение или выход из теста
    выбор = input("Хотите решить еще один пример? (да/нет): ")
    if выбор.lower() != 'да':
        if выбор.lower() == 'нет':
            продолжить_тест = False

# Вывод статистики
print(f"\nВы ответили верно на {результат['верно']} примеров и неверно на {результат['неверно']} примеров.")

